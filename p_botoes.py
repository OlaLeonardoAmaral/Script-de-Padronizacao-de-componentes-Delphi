# 🔤 Função para decodificar captions do DFM para texto simples
def decodificar_caption(caption_raw):
    caption = caption_raw.replace("&", "").replace(";", "")
    caption = re.sub(r'#(\d{1,3})', lambda m: chr(int(m.group(1))), caption)
    caption = unicodedata.normalize('NFD', caption)
    caption = ''.join(c for c in caption if unicodedata.category(c) != 'Mn')
    return caption.lower().strip()


# 🚀 Função principal que aplica padronização de tamanho e ícones nos botões
def padronizar_botoes(caminho_pasta):
    print(f"🔍 Iniciando padronização na pasta: {caminho_pasta}\n")

    arquivos_modificados = 0
    botoes_modificados = 0

    for root, dirs, files in os.walk(caminho_pasta):
        for file in files:
            if not file.lower().endswith('.dfm'):
                continue

            caminho_arquivo = os.path.join(root, file)
            with open(caminho_arquivo, 'r', encoding='latin-1') as f:
                linhas = f.readlines()

            novo_conteudo = []
            dentro_do_botao = False
            caption_padrao = None
            coletando_glyph = False
            glyph_devia_aparecer = False
            modificou_arquivo = False

            for linha in linhas:
                linha_strip = linha.strip()

                # ➕ Detecta início de botão (TcxButton ou TBitBtn)
                if linha_strip.startswith('object') and ('TcxButton' in linha_strip or 'TBitBtn' in linha_strip):
                    dentro_do_botao = True
                    caption_padrao = None
                    coletando_glyph = False
                    glyph_devia_aparecer = False
                    print(f"➡ Botão encontrado em {file}: {linha_strip}")
                    botoes_modificados += 1
                    novo_conteudo.append(linha)
                    continue

                if dentro_do_botao:
                    # 🎯 Ajuste de Width e Height
                    if 'Width =' in linha:
                        linha = '  Width = 80\n'
                        modificou_arquivo = True
                        print("   ↳ Width ajustado para 80")
                    elif 'Height =' in linha:
                        linha = '  Height = 25\n'
                        modificou_arquivo = True
                        print("   ↳ Height ajustado para 25")

                    # 🏷️ Captura o Caption
                    if 'Caption =' in linha:
                        match = re.search(r"Caption\s*=\s*'(.*)'", linha)
                        if match:
                            caption_raw = match.group(1)
                            caption_decod = decodificar_caption(caption_raw)
                            if caption_decod in CAPTION_MAPPING:
                                caption_padrao = CAPTION_MAPPING[caption_decod]["padrao"]
                                glyph_devia_aparecer = True
                                print(f"   ↳ Caption identificado como: {caption_padrao}")

                    # 🚫 Remove glyph antigo se houver
                    if 'OptionsImage.Glyph.Data' in linha:
                        if glyph_devia_aparecer:
                            coletando_glyph = True
                            print("   ↳ Removendo ícone antigo")
                            continue
                        else:
                            novo_conteudo.append(linha)
                            continue

                    if coletando_glyph:
                        if '}' in linha:
                            coletando_glyph = False
                        continue

                    # 🔚 Detecta fim do botão
                    if linha_strip == 'end':
                        if glyph_devia_aparecer and caption_padrao in ICON_GLYPH_DATA:
                            print(f"   ↳ Aplicando ícone padrão: {caption_padrao}")
                            novo_conteudo.extend(ICON_GLYPH_DATA[caption_padrao])
                            modificou_arquivo = True
                        novo_conteudo.append(linha)
                        dentro_do_botao = False
                        caption_padrao = None
                        glyph_devia_aparecer = False
                        continue

                novo_conteudo.append(linha)

            if modificou_arquivo:
                with open(caminho_arquivo, 'w', encoding='latin-1') as f:
                    f.writelines(novo_conteudo)
                arquivos_modificados += 1
                print(f"✅ Arquivo salvo: {file}\n")

    # 📊 Resumo final
    print("\n📦 Resumo da execução:")
    print(f"🗂️ Arquivos modificados: {arquivos_modificados}")
    print(f"🔘 Botões padronizados: {botoes_modificados}")
    print("🏁 Processamento concluído com sucesso!")


